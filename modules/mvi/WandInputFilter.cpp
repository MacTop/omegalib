///////////////////////////////////////////////////////////////////////////////
WandInputFilter* WandInputFilter::createAndInitialize()
{
	WandInputFilter* instance = new WandInputFilter();
	ModuleServices::addModule(instance);
	instance->doInitialize(Engine::instance());
	return instance;
}

